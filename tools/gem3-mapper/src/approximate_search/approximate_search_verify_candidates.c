#define PROFILE_LEVEL PMED

/*
 * Verify Candidates
 */
void approximate_search_verify_candidates(
    approximate_search_t* const search,
    matches_t* const matches) {
  gem_cond_debug_block(DEBUG_SEARCH_STATE) {
    tab_fprintf(stderr,"[GEM]>ASM::Verify Candidates\n");
    tab_global_inc();
  }
  // Parameters
  pattern_t* const pattern = &search->pattern;
  filtering_candidates_t* const filtering_candidates = search->filtering_candidates;
  // Verify Candidates
  filtering_candidates_verify_candidates(filtering_candidates,pattern,matches);
  filtering_candidates_align_candidates(filtering_candidates,pattern,matches);
  search->processing_state = asearch_processing_state_candidates_verified;
  gem_cond_debug_block(DEBUG_SEARCH_STATE) { tab_global_dec(); }
}
/*
 * Verify Extend Candidate
 */
void approximate_search_verify_extend_candidate(
    filtering_candidates_t* const filtering_candidates,
    pattern_t* const candidate_pattern,
    const match_trace_t* const extended_match,
    mapper_stats_t* const mapper_stats,
    paired_matches_t* const paired_matches,
    const sequence_end_t candidate_end) {
  return filtering_candidates_extend_match(filtering_candidates,
      candidate_pattern,extended_match,paired_matches,candidate_end,mapper_stats);
}
/*
 * Kmer-filter Buffered
 */
void approximate_search_kmer_filter_buffered_copy(
    approximate_search_t* const search,
    gpu_buffer_kmer_filter_t* const gpu_buffer_kmer_filter) {
  // Add to GPU-Buffer
  filtering_candidates_buffered_kmer_filter_add(
      search->filtering_candidates,&search->filtering_candidates_buffered,
      &search->pattern,gpu_buffer_kmer_filter,&search->gpu_buffer_kmer_filter_offset);
}
void approximate_search_kmer_filter_buffered_retrieve(
    approximate_search_t* const search,
    gpu_buffer_kmer_filter_t* const gpu_buffer_kmer_filter) {
  // Retrieve from GPU-Buffer
  filtering_candidates_buffered_kmer_filter_retrieve(
      search->filtering_candidates,&search->filtering_candidates_buffered,
      &search->pattern,gpu_buffer_kmer_filter,search->gpu_buffer_kmer_filter_offset);
}
