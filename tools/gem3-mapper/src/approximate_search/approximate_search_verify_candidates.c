/*
 * BPM-Align Buffered
 */
void approximate_search_bpm_align_buffered_update(
    approximate_search_t* const search) {
  // Update number of internal statistics
	filtering_candidates_buffered_bpm_align_set_num_canonical_candidates(
      search->filtering_candidates,&search->filtering_candidates_buffered,
      &search->pattern);
}
void approximate_search_bpm_align_buffered_copy(
    approximate_search_t* const search,
    gpu_buffer_bpm_align_t* const gpu_buffer_bpm_align) {
  // Add to GPU-Buffer
  filtering_candidates_buffered_bpm_align_add(
      search->filtering_candidates,&search->filtering_candidates_buffered,
      &search->pattern,gpu_buffer_bpm_align,&search->gpu_buffer_bpm_align_offset);
}
void approximate_search_bpm_align_buffered_retrieve(
    approximate_search_t* const search,
    gpu_buffer_bpm_align_t* const gpu_buffer_bpm_align,
    matches_t* const matches) {
  // Retrieve
  filtering_candidates_buffered_bpm_align_retrieve(
      search->filtering_candidates,&search->filtering_candidates_buffered,
      &search->pattern,matches,gpu_buffer_bpm_align,
      search->gpu_buffer_bpm_align_offset);
  // Align
  filtering_candidates_align_candidates(
      search->filtering_candidates,&search->pattern,matches);
  // Update state
  search->processing_state = asearch_processing_state_candidates_verified;
}

